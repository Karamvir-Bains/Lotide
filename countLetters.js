// Takes in a string and returns a object with an count of each letter in the string
const countLetters = function(sentence) {
  let results = {};
  for (let letter of sentence) {
    if (letter !== " ") {
      if (results[letter]) {
        results[letter] += 1;
      } else {
        results[letter] = 1;
      }
    }
  }
  return results;
};

